#include<stdio.h>
int main()
{
	int i = 12345;
	float x = 345.678;

	printf(":%3d:%5d:%8d:\n\n",i, i, i);	
	printf(":%3f:%10f:%13f:\n\n",x, x, x);	
	printf(":%3e:%13e:%16e:\n\n",x, x, x);	

	printf(":%3G:%10G:%13G:\n\n",x, x, x);	
	printf(":%3G:%13G:%16G:\n\n",x, x, x);	


	return 0;
}

/*
OUTPUT

:12345:12345:   12345:

:345.678009:345.678009:   345.678009:

:3.456780e+02: 3.456780e+02:    3.456780e+02:

:345.678:   345.678:      345.678:

:345.678:      345.678:         345.678:
*/