#include<stdio.h>
int main()
{
	float x = 123.456;

	printf(":%7f:%7.3f:%7.1f:\n", x, x, x);
	printf(":%12e:%12.5e:%12.3e:\n", x, x, x);

	printf(":%f:%.3f:%.1f:\n", x, x, x);
	printf(":%e:%.5e:%.3e:\n", x, x, x);

	return 0;
}

